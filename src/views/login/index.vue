<template>
  <div class="login-container">
    <el-form
      ref="loginForm"
      :model="loginForm"
      :rules="loginRules"
      class="login-form"
      auto-complete="on"
      label-position="left"
    >
      <div class="title-container">
        <img src="@/assets/common/login-logo.png" alt="" />
      </div>

      <el-form-item prop="mobile">
        <span class="svg-container">
          <svg-icon icon-class="user"> </svg-icon
        ></span>
        <el-input
          ref="mobile"
          v-model="loginForm.mobile"
          placeholder="请输入手机号"
          name="mobile"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="请输入密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon
            :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'"
          />
        </span>
      </el-form-item>

      <el-button
        :loading="loading"
        type="primary"
        class="loginBtn"
        style="width: 100%; margin-bottom: 30px"
        @click.native.prevent="handleLogin"
        >登录</el-button
      >

      <div class="tips">
        <span style="margin-right: 20px">手机号: 13800000002</span>
        <span> 密码: 123456</span>
      </div>
    </el-form>
  </div>
</template>

<script>
import { validMobile } from '@/utils/validate'
import { mapActions } from 'vuex'
export default {
  name: 'Login',
  data() {
    const validateMobile = (rule, value, callback) => {
      // rule 规则 value 输入的值  callback 用来给视图中展示具体信息 callback(new Error())
      // 正则.test(要校验的字符串)，如果字符串被正则匹配成功，那么test方法返回true，否则返回false
      validMobile(value)
        ? callback()
        : callback(new Error('请输入正确的手机号'))
    }
    return {
      // 要绑定的数据对象
      loginForm: {
        mobile: '13800000002',
        password: '123456'
      },
      loginRules: {
        mobile: [
          // { required: true, trigger: 'blur', validator: validateUsername }
          { required: true, trigger: 'blur', message: '请输入手机号' },
          { validator: validateMobile, trigger: 'blur' }
        ],
        password: [
          { required: true, trigger: 'blur', message: '请输入密码' },
          {
            min: 6,
            max: 16,
            trigger: 'blur',
            message: '请输入长度为6-16位的密码'
          }
        ]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function (route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    ...mapActions(['user/login']),
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    // 给登录按钮绑定的方法
    async handleLogin() {
      try {
        // 1.  表单格式校验
        await this.$refs.loginForm.validate()
        // 如果代码能够继续往下走，那么就表示验证成功，否则就是验证失败
        // 验证成功之后就进行登录
        this.loading = true
        // 2. 发起登录请求
        await this.$store.dispatch('user/login', this.loginForm)
        // 如果登录成功就会继续执行后面的代码
        // 3. 页面进行跳转
        this.$router.push('/')
      } catch (err) {
        console.log(err)
      } finally {
        this.loading = false
      }
    }
  }
}
</script>

